#include "libft.h"

size_t	ft_nbrlen(int nbr)
{
	size_t	len;

	len = 0;
	if (nbr <= 0)
	{
		nbr = -nbr;
		len++;
	}
	while (nbr != 0)
	{
		nbr /= 10;
		len++;
	}
	return (len);
}
