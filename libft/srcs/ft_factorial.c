#include "libft.h"

int	ft_factorial(int nb)
{
	int	res;

	if (nb < 0 || nb > 12)
		res = 0;
	else if (nb == 0 || nb == 1)
		res = 1;
	else
		res = nb * ft_factorial(nb - 1);
	return (res);
}
