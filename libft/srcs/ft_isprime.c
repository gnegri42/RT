#include "libft.h"

int	ft_isprime(int nb)
{
	int	tmp;

	if (nb <= 1)
		return (0);
	else if (nb == 2147483647)
		return (1);
	tmp = 2;
	while (tmp < nb)
	{
		if (nb % tmp == 0)
			return (0);
		tmp++;
	}
	return (1);
}
