#include "rt.h"

double	hammersley(int j)
{
	double	x;
	double	i;

	x = 0;
	i = 0.5;
	while (j)
	{
		x += i * (double)(j % 2);
		j /= 2;
		i *= 0.5;
	}
	return (x);
}
